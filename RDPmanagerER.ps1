#region gui events {
$btn_NewPass.Add_Click({
	#read-host "Enter a Password" -assecurestring | convertfrom-securestring | out-file C:\temp\cred.txt
	$ans = Show-AnyBox -Title 'Credentials' -Buttons 'Cancel','Submit' -MinWidth 100 -Prompts @(
